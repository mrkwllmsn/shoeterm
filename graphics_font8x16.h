/* Return a pointer to the 16 rows for cp, or NULL if unsupported. */
static inline const uint8_t *
gfx_glyph8x16(char32_t cp)
{
    if (cp >= 0x20 && cp <= 0x7e)
        return gfx_font8x16[cp - 0x20];
    return NULL;
}

